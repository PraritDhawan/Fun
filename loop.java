public class loop 
{
    public static void main(String[] args) 
    {
        for(int i=0;i<9;i++)
        {
            System.out.println("*");
        }
        int b=9;    
        while (b<12) 
        {
            System.out.println("%");
                b++;
        }
        do {
            System.out.println(">");
            b++;
        } while (b<15);
     }
 }
    

